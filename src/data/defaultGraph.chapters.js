/** 为边推断章节：显式字段 > 端点较晚出场回目 */
export function inferEdgeChapter(edge, nodeById) {
  const explicit = edge.chapter ?? edge.time ?? edge.appearAt
  if (explicit != null && explicit !== '') return Number(explicit)
  const a = nodeById[edge.source]
  const b = nodeById[edge.target]
  const ca = a?.chapter ?? a?.time ?? a?.appearAt
  const cb = b?.chapter ?? b?.time ?? b?.appearAt
  if (ca == null && cb == null) return null
  if (ca == null) return Number(cb)
  if (cb == null) return Number(ca)
  return Math.max(Number(ca), Number(cb))
}
